package com.gzzy.foodManProgram.common;

public class Packageinfo {
    String projectLogID;
    String projectName;
    float price;
    float allNum;
    float syNum;
    String time_begin;
    String time_end;
    int State;

    public String getProjectLogID() {
        return projectLogID;
    }

    public void setProjectLogID(String projectLogID) {
        this.projectLogID = projectLogID;
    }

    public String getProjectName() {
        return projectName;
    }

    public void setProjectName(String projectName) {
        this.projectName = projectName;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public float getAllNum() {
        return allNum;
    }

    public void setAllNum(float allNum) {
        this.allNum = allNum;
    }

    public float getSyNum() {
        return syNum;
    }

    public void setSyNum(float syNum) {
        this.syNum = syNum;
    }

    public String getTime_begin() {
        return time_begin;
    }

    public void setTime_begin(String time_begin) {
        this.time_begin = time_begin;
    }

    public String getTime_end() {
        return time_end;
    }

    public void setTime_end(String time_end) {
        this.time_end = time_end;
    }

    public int getState() {
        return State;
    }

    public void setState(int state) {
        State = state;
    }
}
